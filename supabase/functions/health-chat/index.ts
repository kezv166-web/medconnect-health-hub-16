import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages, userProfile } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    
    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    console.log("Health chat request received with messages:", messages?.length);

    // Build personalized context from user profile
    let userContext = "";
    if (userProfile) {
      userContext = `\n\nPatient Information:
- Name: ${userProfile.full_name}
- Age: ${userProfile.age}
- Primary Health Condition: ${userProfile.primary_health_condition || "Not specified"}
- Doctor: Dr. ${userProfile.doctor_name} (${userProfile.specialty})
- Hospital/Clinic: ${userProfile.hospital_clinic_name}
- Last Consultation: ${userProfile.last_consultation_date || "Not recorded"}
- Next Follow-up: ${userProfile.next_follow_up_date || "Not scheduled"}

Use this information to provide personalized health advice while maintaining medical safety guidelines.`;
    }

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-2.5-flash",
        messages: [
          {
            role: "system",
            content: `You are a professional AI health assistant powered by Gemini 2.5. Provide concise, evidence-based health information.

IMPORTANT GUIDELINES:
- Keep responses brief and to the point (2-3 sentences maximum)
- Use professional medical terminology appropriately
- Always state that advice is informational only and not a substitute for professional medical consultation
- Never diagnose conditions or prescribe treatments
- For serious symptoms, immediately advise consulting a healthcare provider
- Maintain a professional, clinical tone while remaining accessible
- Reference the patient's medical history when relevant${userContext}`,
          },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      const errorText = await response.text();
      console.error("AI gateway error:", response.status, errorText);
      
      if (response.status === 429) {
        return new Response(
          JSON.stringify({ error: "Rate limit exceeded. Please try again later." }),
          {
            status: 429,
            headers: { ...corsHeaders, "Content-Type": "application/json" },
          }
        );
      }
      
      if (response.status === 402) {
        return new Response(
          JSON.stringify({ error: "AI usage limit reached. Please contact support." }),
          {
            status: 402,
            headers: { ...corsHeaders, "Content-Type": "application/json" },
          }
        );
      }

      return new Response(
        JSON.stringify({ error: "AI service error" }),
        {
          status: 500,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        }
      );
    }

    return new Response(response.body, {
      headers: {
        ...corsHeaders,
        "Content-Type": "text/event-stream",
        "Cache-Control": "no-cache",
        "Connection": "keep-alive",
      },
    });
  } catch (error) {
    console.error("Health chat error:", error);
    return new Response(
      JSON.stringify({ error: error instanceof Error ? error.message : "Unknown error" }),
      {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      }
    );
  }
});
